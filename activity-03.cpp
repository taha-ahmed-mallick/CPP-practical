#include <iostream>
using namespace std;

int main()
{
	cout<<"\n\n\t  Size of Fundamental Data Types \n";
	cout<<"\t===============================================\n";
	cout<<"\tThe size of character is: \t\t"<<sizeof(char)<<" bytes \n";
	cout<<"\tThe size of integer is: \t\t"<<sizeof(int)<<" bytes \n";
	cout<<"\tThe size of long is: \t\t\t"<<sizeof(long)<<" bytes \n";
	cout<<"\tThe size of float is: \t\t\t"<<sizeof(float)<<" bytes \n";
	cout<<"\tThe size of double is: \t\t\t"<<sizeof(double)<<" bytes \n";
	cout<<"\tThe size of boolean is: \t\t"<<sizeof(bool)<<" bytes \n";
	return 0;
}
